    private void insertion_mvt(){
       String heure_depart= hhdep.getSelectedItem().toString()+":"+mmdep.getSelectedItem().toString();
       String heure_arrive= hhret.getSelectedItem().toString()+":"+mmret.getSelectedItem().toString();
        try{
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date dm= date_mvt.getDate();
            String datemvt=sdf.format(dm);
            stmt=cnx.obtenircon().createStatement();
            stmt.executeUpdate("insert into mvt_vehicule (num_serie, num_chauffeur, num_vehicule, serie, marque, date_mvt, objet_mvt, trajet, nom_chauffeur, km_depart, km_arrive, heure_depart, heure_arrive) values ('"+Integer.parseInt(num_serie.getText())+"','"+Integer.parseInt(matricule_chauffeur.getText())+"','"+Integer.parseInt(num_veh.getText())+"','"+Integer.parseInt(serie.getText())+"','"+marque.getText()+"','"+datemvt+"','"+objetmvt.getText()+"','"+trajet.getText()+"','"+nom_chauffeur.getText()+"','"+Integer.parseInt(km_depart.getText())+"','"+Integer.parseInt(km_arrive.getText())+"','"+heure_depart+"','"+heure_arrive+"')");
            matricule_chauffeur.setText("");
            num_veh.setText("");
            serie.setText("");    
            objetmvt.setText("");
            trajet.setText("");
            nom_chauffeur.setText("");
            km_depart.setText("");
            km_arrive.setText("");
            
            JOptionPane.showMessageDialog(null, "opération réussi");
        }   
        catch(SQLException e2){
            JOptionPane.showMessageDialog(null, "erreur d'insertion");
        }
    }
