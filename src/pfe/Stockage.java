private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
try{
    stmt=cnx.obtenircon().createStatement();
    
    int row=jTable3.getSelectedRow();
    String table = (jTable3.getModel().getValueAt(row,0).toString());
    ResultSet rs =stmt.executeQuery("select * from produits where code_produit="+table);
    if(rs.next()){
        String add1=rs.getString("code_produit");
        code.setText(add1);
        String add2=rs.getString("designation");
        designation.setText(add2);
        String add3=rs.getString("qte");
        qte.setText(add3);
        String add4=rs.getString("prixu");
        prix.setText(add4);
        type.setSelectedItem(rs.getString("type"));
    }}
    catch(Exception e){
    JOptionPane.showMessageDialog(null, e);
}
}//GEN-LAST:event_jTable3MouseClicked

