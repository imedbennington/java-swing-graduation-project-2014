private void affiche(){
    DefaultTableModel table=(DefaultTableModel) jTable3.getModel();
    if(table.getRowCount()==0){
    try{
        
    stmt=   (Statement) cnx.obtenircon().createStatement();
    ResultSet rslt=stmt.executeQuery("select * from produits");
    while(rslt.next()){
         
    table.addRow(new Object[] {rslt.getObject("code_produit"),rslt.getObject("designation"),rslt.getObject("qte"),rslt.getObject("prixu"),rslt.getObject("type")});
        //i++;
    }
    
    
}                                        
catch(SQLException exp){
    JOptionPane.showMessageDialog(null,"erreur d'affichage");
}
    }else{JOptionPane.showMessageDialog(null,"le tableau n'est pas vide");}
}
