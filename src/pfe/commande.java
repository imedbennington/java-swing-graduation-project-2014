private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
DefaultTableModel table=(DefaultTableModel) tableprod.getModel();
    try{
            stmt=cnx.obtenircon().createStatement();
            ResultSet rslt = stmt.executeQuery("select * from produits");
            while(rslt.next()){
            table.addRow(new Object[] {rslt.getObject("code_produit"),rslt.getObject("designation"),rslt.getObject("qte"),rslt.getObject("type")});
            }
           stmt.close();
           
}catch(SQLException e){
    JOptionPane.showMessageDialog(null, e);
    
}
}//GEN-LAST:event_jButton1ActionPerformed

