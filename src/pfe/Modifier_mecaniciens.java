private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
try{
    stmt=cnx.obtenircon().createStatement();
    String stringmat=mat_mec.getText();
    
    int mat=Integer.parseInt(stringmat);
    System.out.println(mat);
    ResultSet result=stmt.executeQuery("select * from mecanicien as MC where MC.num_mecanicien ="+mat);
    while(result.next()){
        num_cin.setText(result.getString("cin"));
        text_nom.setText(result.getString("nom_mecanicien"));
        text_prenom.setText(result.getString("prenom_mecanicien"));
        text_grade.setText(result.getString("grade"));
        text_experience.setText(result.getString("experience"));
        date_debut.setText(result.getString("date_debut_contrat"));
        date_fin.setText(result.getString("date_fin_contrat"));
        date_premier.setText(result.getString("date_premier_contrat"));
        salaire.setText(result.getString("salaire"));
    }
    }catch(SQLException exp){
        JOptionPane.showMessageDialog(null, "erreur");
    }
}//GEN-LAST:event_jButton4ActionPerformed

