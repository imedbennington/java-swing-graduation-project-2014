private double selectfrais(){
double frais_auto=0;
    try{
    stmt=cnx.obtenircon().createStatement();
    ResultSet rst = stmt.executeQuery("select frais_tot from maintenance where num_serie='"+Integer.parseInt(num_serie.getText())+"'");
    while(rst.next()){
        frais_auto=rst.getDouble("frais_tot");
    }
}catch(SQLException e){
    JOptionPane.showMessageDialog(null, e);
}
return frais_auto;
}
private int selectserie(){
int res=0;int nums=0;
try{
stmt=cnx.obtenircon().createStatement();
ResultSet rst=stmt.executeQuery("select num_serie from maintenance");
while(rst.next()){
  nums=rst.getInt("num_serie");  
}
if(nums==Integer.parseInt(num_serie.getText())){ res=1;}
}catch(SQLException e){
    JOptionPane.showMessageDialog(null, e);
}
return res;
}
